                <h2>ABSTRAK</h2>
                <P>     Pendidikan karakter adalah upaya mewujudkan generasi bangsa yang cerdas dan baik 
                    (smart and good citizenship) atau memiliki ahlak mulia dan berkepribadian Indonesia. 
                    Keberhasilan pendidikan karakter mengisyaratkan pembelajaran tidak serta merta dilihat dari 
                    pesepektif ranah kognitif saja melainkan bagaimana keseimbangan ranah kognitif, afektif, dan 
                    psikomotor yang muaranya adalah mewujudkan manusia seutuhnya. Kondisi pandemi Covid-19 
                    saat ini menjadi tantangan bagi dunia pendidikan khususnya pendidikan formal dalam upaya 
                    pendidikan karakter bangsa. Pembelajaran dominan tidak dilakukan dengan tatap muka, 
                    sehingga menjadi tantangan guru dalam proses pendidikan karakter tersebut. Disisi lain akan 
                    memberikan kesempatan bagai peserta didik dalam mengaktualisasikan nilai-nilai karakter di 
                    masyarakat dalam upaya keikutsertaan pencegahan dan penanggulangan Covid-19. Penelitian 
                    ini bersifat deskriptif kualitatif dengan studi literatur yang berusaha memberikan solusi 
                    bagaimana pendidikan karakter dilakukan ketika pembelajaran masih berlangsung dengan 
                    metode daring pada sekolah menengah pertama. Strategi yang ditawarkan adalah strategi 
                    pendidikan karakter multiplle intelligences berbasis portofolio.</P>
                <h3>Pendahuluan</h3>                
                <p>     keberhasilan proses pendidikan tidak terlepas dari bagaimana proses perencanaan, 
                    implementasi serta kebijakan penunjang yang dilakukan secara berkesinambungan. Karena 
                    pendidikan adalah modal dasar pembangunan maka setiap negara sudah barang tentu 
                    menempatkannya pada tujuan utama. Hal ini juga sesuai dengan tujuan terbentuknya Negara 
                    Kesatuan Republik Indonesia yang akhirnya tertuang dalam pembukaan UUD 1945 alenia IV, 
                    diantaranya adalah “Mencerdaskan Kehidupan Bangsa”. Karena para founding fathers sadar 
                    bahwa pendidikan adalah sarana utama dalam mengubah peradaban bangsa ke arah yang 
                    lebih baik. </p>
                <p>Sesuai UU No. 20 tahun 2003 dijelaskan Pendidikan adalah usaha sadar dan terencana 
                    untuk mewujudkan suasana belajar dan proses pembelajaran agar peserta didik secara aktif 
                    mengembangkan potensi dirinya untuk memiliki kekuatan spiritual keagamaaan, pengendalian 
                    diri, kepribadian, kecerdasan, akhlak mulia, serta ketrampilan yang diperlukan dirinya, 
                    masyarakat, bangsa, dan Negara. Oleh karena itu keberhasilan suatu proses pembelajaran 
                    ditentukan oleh faktor guru, sarana-prasarana,lingkungan dan sudah tentu peserta didik itu 
                    sendiri, memiliki kemauan atau motivasi untuk dapat secara aktif mengembangkan potensi 
                    dirinya. Sehingga tujuan pendidikan dalam rangka mempersiapkan generasi yang unggul 
                    berdaya saing dan memiliki kepribadian atau karater bangsa dapat secara optimal dicapai 
                    sesuai amanat undang-undang tersebut. </P>
                    <P>Tujuan pendidikan adalah bagaimana membentuk generasi yang seutuhnya artinya 
                    memiliki kecerdasan intelektual,sikap yang baik dan dengan keterampilan yang diperlukan 
                    dalam menjalani hidup di masyarakat. Hal inilah yang menjadi tugas guru dalam melaksanakan 
                    proses pembelajaran sebagai bagian dari proses pendidikan untuk dapat menghasilkan 
                    pembelajaran yang outputnya adalah keseimbangan capaian kognitif, afektif atau sikap dan 
                    psikomotor. Oleh karena itu dalam proses pembelajaran kewajiban dan peran guru sangatlah 
                    vital, guru harus mampu sebagai fasilitator maupu mengidentifikasi segala keunggulan dan 
                    kelemahan model-model pembelajaran yang akan diterapkan sehingga benar-benar 
                    menciptakan suatu pembelajaran yang efektif, karena guru “mengajar pada dasarnya 
                    merupakan suatu usaha untuk menciptakan kondisi atau sistem lingkungan yang mendukung 
                    dan memungkinkan untuk berlangsungnya proses belajar”(Sardiman, 2011:47)</P>
                    <P>Pada masa pandemi Covid-19 ini Pemerintah mengeluarkan kebijakan tentang 
                    bagaimana pelaksanaan pembelajaran daring dan luring. Dalam Kamus Besar Indonesia 
                    diartikan dalam jaringan, terhubung melalui jejaring komputer, internet, dan sebagainya. 
                    Pembelajaran during dilaksanakan sebagai langkah tepat untuk dapat mencegah dan menekan 
                    penularan virus Covid-19, pun peserta didik tidak akan ketinggalan pelajaran sebagaimana 
                    yang telah direncanakan dalam kurikulum selama satu tahun ajaran. Walupun pemerintah 
                    sudah mengeluarkan kebijakan New Normal yang tujuannya adalah mengidupkan kembali 
                    sektor perekonomian yang sudah kurang lebih 3 bulan lumpuh akibat dampak Covid-19, akan 
                    tetapi sektor pendidikan khususnya pembelajaran di sekolah belum sepenuhnya berani dibuka 
                    oleh pemerintah. Hal ini dikarenakan anak usia sekolah adalah anak yang cendrung masih labil 
                    dan senang akan berkumpul dengan teman-temannya sehingga memungkinkan terjadinya 
                    penyebaran virus tersebut. Oleh karena itu pembelajaran yang dilakukan saat ini bersifat during 
                    yang sifatnya jarak jauh. Sudah barang tentu menjadi tantangan tersendiri bagi guru dalam 
                    rangka capaian hasil belajar terutama dalam usahan pendidikan karakter anak. </p>
                <h4>1.Metode Penelitian</h4>
                <p>Penelitian ini bersifat deskriptif dengan teknik pengumpulan data berupa studi literatur 
                    dari berbagai referensi yang relevan dengan gejala yang diamati yaitu pada pendidikan karakter 
                    dalam pembelajaran daring untuk anak SMA yang saat ini sedang mengalamai usia peralihan. 
                    Data yang terkumpul dianalisis secara deskriptif kualitatif (Santika dkk, 2019:91, Santika, 
                    2020:29). Tujuannya adalah dapat dilihat strategi pendidikan karakter bagi anak usia SMA 
                    pada masa Pandemi covid- 19 atau saat ini New Normal. Menganalisis Strategi pengintegrasian 
                    pendidikan karakter pada pembelajaran during yang saat ini sebagai alternatif supaya proses 
                    bengajar mengajar dalam pemenuhan tuntutan kurikulum dapat tercapai.</p>
                <h5>2.Hasil dan pembahasan</h5>
                <p>Pendidikan karakter adalah suatu hal yang mutlak harus dilaksanakan karena pada 
                    dasarnya semua guru sebagai pendidik memiliki tujuan yang sama dalam membentuk karakter 
                    bangsa. Tidak serta merta pendidikan karakter menjadi tanggungjawab dari pendidikan moral 
                    atau budi pekerti dan pendidikan Pancasila (Santika, 2019:79), melainkan menjadi tanggung 
                    jawab semua bidang studi. Oleh karena itu ketika pelaksanaan kurikulum 2013, keseimbangan 
                    ranah pembelajaran antatar kognitif, afektif dan psikomotor menjadi ouput yang mutlak sebagai 
                    bagian penidikan karakter bangsa.
                        Karakter adalah watak seseorang, atau ahlak yang diperoleh dari internalisasi dengan 
                    lingkungannya. Karakter seseorang akan menjadi baik apabila didasarkan dengan nilai-nilai 
                    moral dan etika yang berlaku dan disepakati di masyarakat. Lickona (1992) “menekankan 
                    pentingnya tiga komponen karakter yang baik (components of good character), yaitu moral 
                    knowing atau pengetahuan tentang moral, moral feeling atau perasaan tentang moral, dan 
                    moral action atau perbuatan moral”. Karakter yang baik akan muncul setelah ketiga kompenen 
                    karakter tersebut bisa terpenuhi dalam diri peserta didik. Lebih lanjut Nopan Omeri (2015) 
                    menyatakan Karakter merupakan perpaduan antara moral, etika, dan akhlak. Moral lebih 
                    menitikberatkan pada kualitas perbuatan, tindakan atau perilaku manusia atau apakah 
                    perbuatan itu bisa dikatakan baik atau buruk, atau benar atau salah. Sebaliknya, etika 
                    memberikan penilaian tentang baik dan buruk, berdasarkan norma-norma yang berlaku dalam 
                    masyarakat tertentu, sedangkan akhlak tatanannya lebih menekankan bahwa pada hakikatnya 
                    dalam diri manusia itu telah tertanam keyakinan di mana keduanya (baik dan buruk) itu ada. 
                    Faktor-faktor ini saling berkaitan dan mendukung satu sama lainnya dalam membentuk 
                    kepribadian seorang anak. </p>
                    <p>     Pembelajaran daring, atau dalam jaringan, adalah terjemahan dari istilah online yang 
                        bermakna tersambung ke dalam jaringan komputer. Dengan akata lain merupakan 
                        pembelajaran tanpa tatap muka secara langsung antara guru dan siswa, tetapi dilakukan 
                        melalui jaringan internet (online) dari tempat yang berdeda-beda. Menururt Astra Winaya (2020) 
                        dalam Webminar Nasional PGSD Universitas Dwijendra, Pembelajaran dilakukan melalui video 
                        conference, e-learning atau distance learning. Lebih lanjut Ditjen Guru dan Tenaga 
                        Kependidikan Kementrian Pendidikan dan Kebudayaan dalam guru pembelajaran petunjuk 
                        teknis peningkatan program peningkatan kompetensi guru pembelajar moda dalam jaringan 
                        tahun 2016 menjelaskan Pendekatan pembelajaran pada Guru Pembelajar moda daring 
                        memiliki karakteristik sebagai berikut: 1. Menuntut pembelajar untuk membangun dan 
                        menciptakan pengetahuan secara mandiri (constructivism); 2. Pembelajar akan berkolaborasi 
                        dengan pembelajar lain dalam membangun pengetahuannya dan memecahkan masalah 
                        secara bersama-sama (social constructivism); 3. Membentuk suatu komunitas pembelajar 
                        (community of learners) yang inklusif; 4. Memanfaatkan media laman (website) yang bisa 
                        diakses melalui internet, pembelajaran berbasis komputer, kelas virtual, dan atau kelas digital; 
                        5. Interaktivitas, kemandirian, aksesibilitas, dan pengayaan;
                        Kelebihan pembelajaran daring diantaranya adalah, 1. Pembelejaran tidak memerlukan 
                        ruang kelas, karena proses pembelajaran berlangsung dari rumah atau jarak jauh. Siswa di 
                        tempat atau lingkungan masing-masing yang dapat menciptakan suasana belajar dengan 
                        fasilitas internet yanga ada., 2. Guru tidak perlu tatap muka secara langsung di depan kelas, 
                        karena yang digunakan adalah fasilitas komputer yang dihubungkan dengan internet. 3. Tidak 
                        terbatas waktu maksudnya adalah pembelajaran bisa dilakukan kapanpun, dimanapun sesuai 
                        dengan kesepakatan selama lingkungan dan fasilitas mendukung untuk terlaksananya proses 
                        pembelajaran moda daring tersebut. Oleh karena itu mode pembelajaran daring ini bisa 
                        dikatakan lebih efisien dan efektif apabila suprastruktur dan infra struktur tersedia dengan baik.
                    <h6>3.Kesimpulan</h6>
                    <p>Berdasarkan hasil penelitian dapat disimpulkan beberapa hal. Pertama, Pendidikan 
                        karakter memiliki tiga fungsi utama. Pertama, fungsi pembentukan dan pengembangan potensi. 
                        Pendidikan karakter membentuk dan mengembangkan potensi siswa agar berpikiran baik, 
                        berhati baik, dan berperilaku sesuai dengan falsafah pancasila. Kedua, fungsi perbaikan dan 
                        penguatan. Pendidikan karakter memperbaiki dan memperkuat peran keluarga, satuan 
                        pendidikan, masyarakat, dan pemerintah untuk ikut berpartisipasi dan bertanggung jawab 
                        dalam pengembangan potensi warga negara dan pembangunan bangsa menuju bangsa yang 
                        maju, mandiri, dan sejahtera. Ketiga, fungsi penyaring. Pendidikan karakter memilah budaya 
                        bangsa sendiri dan menyaring budaya bangsa lain yang tidak sesuai dengan nilainilai budaya 
                        bangsa dan karakter bangsa yang bermartabat. Guru dalam megengbangkan materi 
                        pembelajaran harus mengananlisis materi pembelajaran yang disesuaikan dengan masing-masing nilai karakter. Tujuannya adalah antara materi pembelajaran dengan output yang di 
                        hasilkan sesuai dengan kebutuhan di masyarakat. Kedua, Pembelajaran daring, atau dalam 
                        jaringan, adalah terjemahan dari istilah online yang bermakna tersambung ke dalam jaringan 
                        komputer. Dengan kata lain merupakan pembelajaran tanpa tatap muka secara langsung 
                        antara guru dan siswa, tetapi dilakukan melalui jaringan internet (online) dari tempat yang 
                        berdeda-beda. Ketiga, Prinsip strategi Multiple Intelligences pada pendidikan karakter masih 
                        menggunakan prinsip pendekatan pembelajaran kontruktivistik. Peserta didik secara aktif 
                        mengembangan kedelapan potensi yang dimiliki disesuaikan dengan kompetensi dasar yang 
                        diajarkan dan bagaimana aktualisasinya terutama jika ada kaitan dalam menghadapi Covid-19. 
                        Strategi implementasi pendidikan karakter melalui multiple intelligences berbasis portofolio 
                        dengan diitegrasikan pada mata pelajaran merupakan suatu upaya dalam proses pembelajaran 
                        untuk dapat mengembangkan life skill atau kecakapan peserta didik.
                        </p>
